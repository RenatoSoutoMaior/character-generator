package com.rsm.objectorientationwithrpg.model.hero;

public class Hunter extends Hero {

    public Hunter(String name, Gender gender) {
        this.name = name;
        this.gender = gender;
        this.vitality = 115;
        this.stamina = 75;
        this.strength = 1;
        this.dexterity = 2;
        this.endurance = 1;
        this.intelligence = 3;
        this.agility = 2;
        this.alertness = 3;
        this.luck = 1;
    }
}
